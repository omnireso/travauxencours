                      <h2>L'artiste vu par le webmaster</h2>
                      
                      <p>
                      <img src="philippe-morel.jpg" width="243" height="325" alt="L'artiste Philippe Morel devant ses tableaux">
                      <br>Crédit photo : Mathely Gérard, pour le Journal La Montagne, Source : <a href="http://www.lamontagne.fr/auvergne/actualite/departement/puy-de-dome/puy-de-dome-local/2015/03/26/philippe-morel-expose-a-la-maison-grenouille_11380116.html">http://www.lamontagne.fr/auvergne/actualite/departement/puy-de-dome/puy-de-dome-local/2015/03/26/philippe-morel-expose-a-la-maison-grenouille_11380116.html</a>
                      </p>
                      
                      
                    
                      
                                <p>Né en 1950 mais pas encore mort, <em>Philippe Morel</em> est donc par définition un artiste authentiquement contemporain, ce qu'il revendique au travers de la projection bien actuelle de son trait artistique, qui, tel une saillie picturale, bouleverse nos convictions sur le post-modernisme.</p>
                                
                                <p><em>Philippe Morel</em> s'exerce à la retranscription artistique du monde qu'il cotoie depuis presque un demi-siècle, et le processus de maturation de sa démarche d'artiste-peintre amateur le place dans un travail de recherche continu. Après s'être confronté à l'interprétation et la matéralisation de sa vision cosmique du décor quotidien (<a href="toiles-natures-mortes.php" hreflang="fr">natures mortes</a>, portraits, animaux...) il s'inscrit depuis quelques années dans une quête de l'image tendant vers l'abstrait.</p>

                                <p>Les visiteurs curieux et attentifs auront l'opportunité de suivre le parcours du peintre vers cette recherche abstraite.</p>
                                
                                <p>Reconnu par sa famille pour ses prouesses sportives (il manie aussi bien le pinceau et la palette de couleurs que la pédale), il affectionne particulièrement la couleur jaune en hommage au maillot qu'il rêve de porter sur les routes sinueuses et pentues de sa région Rhône-Alpes-Auvergne.</p>
                                
                                                          
                                <p>Reconnu par ses pairs, il a obtenu de nombreux titres honorifiques au cours de réunions familiales pour la globalité de son oeuvre, ainsi que de nombreux encouragements, <a href="http://www.philmorel.com/presse.php" hreflang="fr">prix, distinctions et récompenses</a> de la part de comités régionaux.</p>
                                
                                                     
