                                <h2>L'artiste vu par lui-même</h2>
                                
                                
                                <p>Peintre autodidacte, membre de l'association des Artistes de Chamalières, je pratique essentiellement la <strong>peinture à l'huile et l'acrylique</strong>, cependant le pastel me permet de varier les approches et d'enrichir ma palette de coloris.</p>
	

<p>Ma <strong>peinture figurative</strong> est largement inspirée par les <a href="tableaux-auvergne-paysages.php" hreflang="fr">paysages auvergnats</a>, avec une préférence pour les éclairages très contrastés, ciels d'orage sur la Limagne, champs de colza ou de tournesols. Fôrets, reflets sur l'eau et <a href="tableaux-fleurs.php" hreflang="fr">fleurs</a> constituent d'autres sources d'inspiration. Ma démarche consiste à rechercher la simplification en  renforçant les lignes et les contrastes. <em>Cézanne</em>, <em>Matisse</em> et <em>Nicolas de Staël</em> ont toujours été des modèles pour moi. Je commence à m'intéresser à une démarche vers l'abstraction pour renforcer la composition des toiles et faire évoluer mon approche des paysages.</p>


<p>Je participe depuis plusieurs années aux expositions collectives organisées par les Artistes Chamaliérois et par l'ORACLE, Clermont -Ferrand Chamalières et de nombreuses autres communes du département, dans lesquelles j'ai obtenu plusieurs distinctions.</p>

<p>Les expositions personnelles récentes (pour une liste exhaustive des dates, consulter la rubrique <a href="http://www.philmorel.com/actu.php" hreflang="fr">actu</a> :</p>

<p>Office du tourisme de Pont du château (2014), La maison Grenouille à Pérignat sur Allier (2015), Usson (63) un des plus beaux villages de France (2015) , Marcillac la Croisille en Corrèze (2015), Chatel Guyon (2015).
</p>
 
 
                
          
